package pertemuan2.tugas.no3;

/**
 *
 * @author Asus
 */
public class Lingkaran {

    double phi, r;

    double hitungLuas(double phi, double r) {
        return phi * (r * r);
    }

    double hitungKeliling(double phi, double r) {
        return phi * 2 * r;
    }

}
